big.resize((128,128), Image.LANCZOS).save('/tmp/macatrium-128.png')
big.save('/tmp/macatrium.ico', sizes=[(16,16),(32,32),(48,48),(64,64),(128,128),(256,256)])
print("wrote /tmp/macatrium-icon.r and GUI PNGs; ICN#",len(ICN),"icl8",len(icl8),"bytes")
